<script>
    new DataTable('#table', {
        initComplete: function () {
            this.api()
                .columns()
                .every(function () {
                    let column = this;

                    // Create select element
                    let select = document.createElement('select');
                    select.add(new Option(''));
                    column.footer().replaceChildren(select);

                    // Apply listener for user change in value
                    select.addEventListener('change', function () {
                        column
                            .search(select.value, {exact: true})
                            .draw();
                    });

                    // Add list of options
                    column
                        .data()
                        .unique()
                        .sort()
                        .each(function (d, j) {
                            select.add(new Option(d));
                        });
                });
            document.querySelector('.dt-search > label').innerHTML = "Rechercher"
            document.querySelector('.dt-length-0 > label').remove()
            document.querySelector('#table_info').innerHTML = "";
        }
    });


</script>
